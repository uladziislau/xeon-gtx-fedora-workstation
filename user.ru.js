//         RAM 62.7 GB
//         GPU NVIDIA GeForce GTX 1660 Super
// Дисплей: 2K (2560x1440) @ 165 Hz
// ОС:      Fedora 43 KDE (Wayland), драйвер NVIDIA 580.xx
// Профиль: Тяжелые SPA, WebGPU, Google AI Studio, DevTools, многопоточность без оглядки на ресурсы.
// Цель:    Максимальная производительность и плавность, приоритет — скорость и отзывчивость,
//          а не экономия ресурсов или универсальная совместимость.
// =============================================================================
//
// ВАЖНО:
// - Конфиг рассчитан на мощную рабочую станцию с большим запасом по CPU/GPU/RAM.
// - Многие настройки агрессивны и не подходят для слабых машин.
// - Здесь нет debug-флагов, ломающих UI (например, devtools.chrome.enabled).
// - Все параметры согласованы между собой, чтобы не было скрытых конфликтов.
// =============================================================================



// [СЕКЦИЯ 1: ГЛОБАЛЬНАЯ СИНХРОНИЗАЦИЯ И FPS (165 ГЦ НА WAYLAND + NVIDIA)]
// -----------------------------------------------------------------------------

// Жестко задаем частоту кадров движка компоновки.
// ПОЧЕМУ: Firefox/Zen на Linux часто ошибочно определяет 60 Гц или плавающую частоту.
// Это критично для корректной работы TestUFO и визуальной плавности на 165 Гц.
user_pref("layout.frame_rate", 165);

// Включаем взаимодействие с VSync композитора Wayland.
// ПОЧЕМУ: Обеспечивает синхронную подачу кадров в KWin, снижает tearing и рассинхрон фазы.
user_pref("widget.wayland.vsync.enabled", true);

// Принудительно включаем Wayland WebRender compositor, даже если он помечен как "blocklisted".
// ПОЧЕМУ: На NVIDIA по умолчанию Firefox может избегать Wayland-композитора, но в наших условиях
// (мощный GPU, свежий драйвер, KWin) это даёт более стабильный frame pacing и честные 165 Гц.
user_pref("gfx.webrender.compositor.force-enabled", true);

// Отключаем частичную перерисовку (Partial Present).
// ПОЧЕМУ: Заставляем GPU перерисовывать весь кадр каждый раз. Это увеличивает нагрузку на GPU,
// но убирает микро-фризы и артефакты, связанные с неверным определением "грязных" регионов.
user_pref("gfx.webrender.max-partial-present-rects", 0);

// Запрещаем WebRender использовать partial-present через buffer-age.
// ПОЧЕМУ: Без этого флага WebRender может пытаться использовать buffer-age, несмотря на запрет выше,
// что приводит к конфликтам и нестабильному поведению на 165 Гц.
user_pref("gfx.webrender.allow-partial-present-buffer-age", false);



// [СЕКЦИЯ 2: ГРАФИЧЕСКИЙ КОНВЕЙЕР, DMA-BUF И GPU-ПРОЦЕСС]
// -----------------------------------------------------------------------------

// Принудительно включаем DMA-BUF (Zero-Copy).
// ПОЧЕМУ: Текстуры передаются напрямую из браузера в GPU, минуя лишние копирования через RAM.
// Это особенно важно для 2K @ 165 Гц, где объём данных на кадр велик.
user_pref("widget.dmabuf.force-enabled", true);

// Включаем WebRender для всех элементов интерфейса и контента.
// ПОЧЕМУ: Полное аппаратное ускорение отрисовки, без fallback на старые слои.
user_pref("gfx.webrender.all", true);

// Включаем отдельный GPU-процесс для отрисовки.
// ПОЧЕМУ: Если драйвер NVIDIA упадёт, браузер не закроется целиком — перезапустится только GPU-процесс.
user_pref("layers.gpu-process.enabled", true);

// Сообщаем композитору KDE о непрозрачных регионах окна.
// ПОЧЕМУ: KWin может оптимизировать отрисовку, не рисуя то, что гарантированно скрыто за окном браузера.
user_pref("widget.wayland.opaque-region.enabled", true);

// Запрещаем WebRender использовать software fallback.
// ПОЧЕМУ: На мощной GPU нет смысла падать на CPU-рендеринг. Такие переключения вызывают рывки и
// нестабильный FPS. Мы требуем, чтобы всё рендерилось через GPU.
user_pref("gfx.webrender.fallback.software", false);

// Запрещаем использование software-драйверов.
// ПОЧЕМУ: Гарантируем, что будет использоваться только полноценный GPU-драйвер.
user_pref("gfx.webrender.reject-software-driver", true);

// Включаем Warp-пайплайн для WebRender.
// ПОЧЕМУ: Warp уменьшает pipeline-stall на NVIDIA, особенно при резком скролле, открытии DevTools
// и сложных сценах. Это стабилизирует frame pacing и снижает вероятность микрофризов.
user_pref("gfx.webrender.use-warp", true);

// Отключаем дисковый кэш шейдеров WebRender.
// ПОЧЕМУ: Шейдеры будут кэшироваться только в RAM. Это уменьшает I/O по диску и убирает фризы,
// связанные с записью/чтением бинарников шейдеров. На 62+ ГБ RAM это разумный обмен.
user_pref("gfx.webrender.program-binary-disk", false);



// [СЕКЦИЯ 3: WEBGPU (VULKAN BACKEND, AI/SPA НАГРУЗКА)]
// -----------------------------------------------------------------------------

// Включаем поддержку WebGPU.
// ПОЧЕМУ: Необходимо для современных графических веб-приложений, AI-инструментов и WebGPU-демо.
user_pref("dom.webgpu.enabled", true);

// Принудительно указываем использовать Vulkan как backend для WebGPU.
// ПОЧЕМУ: На Linux + NVIDIA Vulkan — нативный и наиболее производительный API.
// OpenGL медленнее, а авто-выбор может ошибаться.
user_pref("dom.webgpu.wgpu-backend", "vulkan");

// Включаем подробные логи оборудования и backend'а в about:support.
// ПОЧЕМУ: Упрощает диагностику, если что-то пойдёт не так с WebGPU.
user_pref("dom.webgpu.hal-labels", true);

// Разрешаем использование WebGPU внутри Service Workers.
// ПОЧЕМУ: Это открывает путь к фоновым вычислениям и сложным SPA/AI-сценариям, работающим в фоне.
user_pref("dom.webgpu.service-workers.enabled", true);

// Отключаем Direct Surface Present для WebGPU.
// ПОЧЕМУ: В прошлых тестах включение прямой презентации вызывало рассинхрон фазы (красные индикаторы
// VSYNC в тестах). Отключение передаёт контроль таймингов обратно Wayland-композитору, что даёт
// более стабильную и "зелёную" картинку.
user_pref("dom.webgpu.direct-surface-present.enabled", false);



// [СЕКЦИЯ 4: СТРАТЕГИЯ "БЕЗЛИМИТНОЙ ПАМЯТИ" (62+ GB RAM, МАКСИМАЛЬНАЯ АГРЕССИЯ)]
// -----------------------------------------------------------------------------

// Полностью отключаем дисковый кэш браузера.
// ПОЧЕМУ: На рабочей станции с 62+ ГБ RAM нет смысла тратить SSD на мелкие кэш-файлы.
// Это уменьшает I/O по диску и делает поведение более предсказуемым.
user_pref("browser.cache.disk.enable", false);

// Включаем кэш в оперативной памяти.
// ПОЧЕМУ: Всё, что можно, должно кэшироваться в RAM — это самый быстрый уровень памяти.
user_pref("browser.cache.memory.enable", true);

// Выделяем 4 ГБ (4194304 КБ) под кэш файлов (скрипты, CSS, HTML).
// ПОЧЕМУ: Тяжёлые SPA, бандлы, WASM и ассеты будут жить в RAM, обеспечивая мгновенную перезагрузку страниц.
user_pref("browser.cache.memory.capacity", 4194304);

// Увеличиваем максимальный размер одного объекта в кэше до 50 МБ.
// ПОЧЕМУ: Крупные JS-бандлы и WASM-модули тоже должны попадать в RAM-кэш, а не игнорироваться.
user_pref("browser.cache.memory.max_entry_size", 51200);

// Выделяем 2 ГБ под декодированные изображения.
// ПОЧЕМУ: Храним изображения в "разжатом" виде, чтобы при быстром скролле не было "пустых" мест
// и задержек на декодирование. Это особенно важно для лент, галерей и дизайн-инструментов.
user_pref("image.mem.max_decoded_image_kb", 2097152);

// Запрещаем выгрузку вкладок при нехватке памяти.
// ПОЧЕМУ: На 62+ ГБ RAM браузер не должен самовольно выгружать вкладки. Мы предпочитаем держать
// всё в памяти, чтобы переключение было мгновенным.
user_pref("browser.tabs.unloadOnLowMemory", false);

// Устанавливаем порог "паники" по памяти на уровне 2 ГБ свободной RAM.
// ПОЧЕМУ: Только при действительно низком остатке памяти браузер начнёт вести себя осторожнее.
user_pref("browser.low_commit_space_threshold_mb", 2048);

// Увеличиваем Back/Forward Cache (BFCache).
// ПОЧЕМУ: Храним до ~16 страниц в "живом" состоянии, чтобы переходы Назад/Вперёд были мгновенными.
user_pref("browser.sessionhistory.max_total_viewers", 16);

// Увеличиваем интервал сохранения сессии до 30 секунд.
// ПОЧЕМУ: Дефолтные 15 секунд создают лишнюю нагрузку на I/O. На рабочей станции можно позволить
// себе более редкие сохранения без потери надёжности.
user_pref("browser.sessionstore.interval", 30000);



// [СЕКЦИЯ 5: ПРОЦЕССЫ, МНОГОПОТОЧНОСТЬ И IPC (XEON 18C/36T, АГРЕССИВНЫЙ РЕЖИМ)]
// -----------------------------------------------------------------------------

// Устанавливаем лимит процессов контента = 24.
// ПОЧЕМУ: У тебя 18 физических ядер. 24 процесса дают:
// - отдельные процессы для тяжёлых вкладок,
// - запас под фоновые вкладки,
// - отдельные процессы под системные задачи,
// при этом не создавая чрезмерного контекстного переключения.
user_pref("dom.ipc.processCount", 24);

// Устанавливаем лимит изолированных процессов (site isolation) также в 24.
// ПОЧЕМУ: Сохраняем баланс между безопасностью, изоляцией и производительностью.
user_pref("dom.ipc.processCount.webIsolated", 24);

// Увеличиваем количество процессов под расширения до 8.
// ПОЧЕМУ: Много DevTools, блокировщиков, помощников и т. д. Не даём им мешать друг другу.
user_pref("dom.ipc.processCount.extension", 8);

// Включаем предзапуск процессов контента.
// ПОЧЕМУ: Процессы вкладок будут заранее подготовлены, что ускоряет открытие новых страниц.
user_pref("dom.ipc.processPrelaunch.enabled", true);

// Уменьшаем задержку предзапуска процессов до нуля.
// ПОЧЕМУ: Процессы вкладок будут готовы мгновенно, что ускоряет открытие новых страниц.
// На Xeon нет смысла ждать 1000 мс — мы хотим максимальную отзывчивость.
user_pref("dom.ipc.processPrelaunch.delayMs", 0);

// Включаем приоритизацию активной вкладки.
// ПОЧЕМУ: Вкладка, с которой ты работаешь прямо сейчас, должна получать CPU первой.
user_pref("dom.ipc.scheduler.useActiveTabPriority", true);

// Отключаем менеджер приоритетов IPC Firefox.
// ПОЧЕМУ: В условиях SCX/оптимизированного ядра Linux планировщик ОС справляется лучше,
// чем встроенный менеджер приоритетов Firefox. Мы убираем лишний уровень логики.
user_pref("dom.ipc.processPriorityManager.enabled", false);

// Включаем preemption в IPC-планировщике.
// ПОЧЕМУ: Разрешаем более агрессивное вытеснение задач внутри IPC, уменьшая задержки между
// процессами контента, GPU-процессом и DevTools.
user_pref("dom.ipc.scheduler.preemption", true);

// Увеличиваем количество IPC-потоков до 8.
// ПОЧЕМУ: Xeon с 36 потоками легко переваривает дополнительный параллелизм в IPC,
// что ускоряет коммуникацию между процессами.
user_pref("dom.ipc.scheduler.threadCount", 8);



// [СЕКЦИЯ 6: JS, WASM И DEVTOOLS (ПРОИЗВОДИТЕЛЬНОСТЬ БЕЗ DEBUG-РЕЖИМОВ)]
// -----------------------------------------------------------------------------

// Включаем параллельный парсинг JavaScript.
// ПОЧЕМУ: Xeon силён в многопоточности. Параллельный парсинг позволяет использовать свободные ядра
// для компиляции скриптов, ускоряя загрузку тяжёлых SPA.
user_pref("javascript.options.parallel_parsing", true);

// Включаем поддержку WebAssembly.
// ПОЧЕМУ: Современные веб-приложения и AI-инструменты активно используют WASM.
// На Xeon это даёт хороший прирост производительности.
user_pref("javascript.options.wasm", true);

// Увеличиваем "high water mark" для JS-кучи до 256 МБ.
// ПОЧЕМУ: Разрешаем JS-куче расти больше перед запуском агрессивного GC. Это уменьшает количество
// пауз на сборку мусора и снижает микро-фризы в сложных SPA.
user_pref("javascript.options.mem.high_water_mark", 256);

// Включаем пре-компиляцию шейдеров WebRender.
// ПОЧЕМУ: Убирает подёргивания при первом появлении новых графических элементов и сложных сцен.
user_pref("gfx.webrender.precache-shaders", true);

// Помечаем, что DevTools уже открывались.
// ПОЧЕМУ: Это ускоряет первый запуск DevTools и убирает некоторые "первичные" задержки.
user_pref("devtools.everOpened", true);

// Отключаем Browser Toolbox и chrome-отладку.
// ПОЧЕМУ: Ранее включённый devtools.chrome.enabled приводил к гигантскому контекстному меню
// и включал режим отладки самого браузера. Мы оставляем DevTools для страниц, но не для UI браузера.
user_pref("devtools.chrome.enabled", false);

// Отключаем удалённую отладку по умолчанию.
// ПОЧЕМУ: Это снижает поверхность атаки и убирает лишние пункты в контекстном меню и настройках.
user_pref("devtools.debugger.remote-enabled", false);



// [СЕКЦИЯ 7: СЕТЬ И LATENCY (HTTP/3, DNS, PREDICTOR)]
// -----------------------------------------------------------------------------

// Принудительно включаем HTTP/3 (QUIC).
// ПОЧЕМУ: Сервисы Google (YouTube, AI Studio и др.) работают быстрее и стабильнее через QUIC (UDP),
// особенно при нестабильных сетях и высоком RTT.
user_pref("network.http.http3.enable", true);

// Ограничиваем количество постоянных соединений на сервер до 10.
// ПОЧЕМУ: Консервативная настройка, предотвращающая перегрузку HTTP/1.1 очередей и улучшая
// предсказуемость поведения при большом количестве вкладок.
user_pref("network.http.max-persistent-connections-per-server", 10);

// Включаем предиктивную загрузку DNS и ресурсов.
// ПОЧЕМУ: Браузер заранее резолвит домены и подготавливает соединения, ускоряя переход по ссылкам.
user_pref("network.predictor.enabled", true);
user_pref("network.dns.disablePrefetch", false);
user_pref("network.predictor.enable-prefetch", true);

// Включаем IPv6, если он доступен.
// ПОЧЕМУ: Многие современные сервисы работают быстрее по IPv6, а не по IPv4.
user_pref("network.dns.disableIPv6", false);



// [СЕКЦИЯ 8: ОТЗЫВЧИВОСТЬ UI, ОТРИСОВКА И СКРОЛЛИНГ]
// -----------------------------------------------------------------------------

// Убираем искусственную задержку перед первой отрисовкой.
// ПОЧЕМУ: По умолчанию браузер ждёт 5–250 мс перед рендерингом, чтобы "накопить" данные.
// На мощном Xeon это не нужно — мы хотим видеть контент как можно раньше.
user_pref("nglayout.initialpaint.delay", 0);

// Отключаем decode-on-draw для изображений.
// ПОЧЕМУ: По умолчанию изображения декодируются при попадании в область видимости (экономия RAM).
// Мы предпочитаем декодировать заранее, чтобы при быстром скролле не было задержек и "пустых" мест.
user_pref("image.mem.decodeondraw", false);

// Включаем физику плавного скроллинга (MSD).
// ПОЧЕМУ: На 165 Гц мониторе скролл должен быть максимально плавным и физически "естественным".
user_pref("general.smoothScroll.msdPhysics.enabled", true);

// Убираем вкладки из заголовка окна.
// ПОЧЕМУ: Визуальное предпочтение и более предсказуемое поведение под KWin.
user_pref("browser.tabs.inTitlebar", 0);

// Всегда показываем панель закладок.
// ПОЧЕМУ: Рабочая станция — это про продуктивность, а не про экономию вертикального пространства.
user_pref("browser.toolbars.bookmarks.visibility", "always");

// Не искажать цвета документа системной темой.
// ПОЧЕМУ: Это исправляет проблемы с тёмными темами в некоторых веб-IDE и графических инструментах.
user_pref("browser.display.document_color_use", 0);



// [СЕКЦИЯ 9: ГИГИЕНА, ТЕЛЕМЕТРИЯ И ПРИВАТНОСТЬ]
// -----------------------------------------------------------------------------

// Отключаем отправку данных о использовании браузера.
// ПОЧЕМУ: На рабочей станции нам важнее предсказуемость и отсутствие фоновой активности,
// чем участие в телеметрии.
user_pref("datareporting.policy.dataSubmissionEnabled", false);

// Отключаем загрузку health-report.
// ПОЧЕМУ: Убираем лишние фоновые запросы и записи.
user_pref("datareporting.healthreport.uploadEnabled", false);

// Отключаем объединённую телеметрию.
// ПОЧЕМУ: Минимизируем сбор и отправку любых статистических данных.
user_pref("toolkit.telemetry.unified", false);

// Отключаем телеметрию целиком.
// ПОЧЕМУ: Максимально "тихий" браузер, без фоновой аналитики.
user_pref("toolkit.telemetry.enabled", false);

// Фиксируем, что глобальный сигнал приватности уже был включён.
// ПОЧЕМУ: Это влияет на поведение некоторых сайтов и трекеров.
user_pref("privacy.globalprivacycontrol.was_ever_enabled", true);
